## Clone nginx from git
sudo git clone https://github.com/nginx/nginx.git /opt/sso/nginx

## Clone  nginx-development-kit
sudo git clone https://github.com/simpl/ngx_devel_kit.git /opt/sso/ngx_devel_kit

## Clone lua-nginx module
sudo git clone https://github.com/openresty/lua-nginx-module.git /opt/sso/lua-nginx

## Clone rds-json-nginx module
sudo git clone https://github.com/openresty/rds-json-nginx-module.git /opt/sso/rds-json-nginx

## Clone lua-cjson module
sudo git clone https://github.com/openresty/lua-cjson.git /opt/sso/lua-cjson

## Clone echo-nginx module
sudo git clone https://github.com/openresty/echo-nginx-module.git /opt/sso/echo-nginx